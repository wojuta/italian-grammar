export const getExerciseTranslation = (id: string): string | undefined => {
  const separator = id.lastIndexOf('-');
  if (separator < 0) return undefined;
  const group = id.slice(0, separator);
  const questionNumber = Number(id.slice(separator + 1));
  return translations[group]?.[questionNumber - 1];
};
